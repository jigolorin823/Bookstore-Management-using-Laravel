     * Display the specified resource.
     *
     * @param  \App\Book  $book
     * @return \Illuminate\Http\Response
     */
    public function show(Book $book)
    {
        $title = $book->title;
        $book_id = $book->id;
        $assignments = Assignment::with('author')->where('assignment_id', "=", $book->assignment_id)->where('status','=','1')->get();
        return View::make('author.index')->with(compact('assignments'))->with(compact('title'))->with(compact('book_id'));
    }

